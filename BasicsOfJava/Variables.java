public class Variables {

    public static void main(String args[]) {
        int a = 20, b = 10;
        System.out.println(a   >   b   & b < a);
        System.out.println(a   >  b   && b < a);
        System.out.println(a   >   b   | b < a);
        System.out.println(a   >  b   || b < a);
        System.out.println(a   >   b   ^ b < a);
        System.out.println(~a);
        System.out.println();
        System.out.println();
        
        int A = 9, B = 12;
        int C = A&B;
        System.out.println(C);

    }

}
